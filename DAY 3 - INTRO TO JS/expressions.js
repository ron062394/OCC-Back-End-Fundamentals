//expressions

//String expression
console.log("Hello" + " " + "World!");

//logical expression
let sunny = false;
let warm = false;

console.log("Is it sunny AND warm?" , sunny && warm);
console.log("Is it sunny OR warm?", sunny ||  warm);
console.log("NOT sunny?", !sunny);


//assignment expression
let a = 20;
let b = 10;

a = a + b;
console.log(a);

//Addition
a += b;
console.log(a);

//Substraction
a -= b;
console.log(a);

//Multiplication
a *= b;
console.log(a);

//Division
a /= b;
console.log(a);